using System;

namespace PracticeCharactersAndStrings
{
    public class PracticeCharactersAndStrings
    {
        public static void Main()
        {
            string stringInput1 = "Software University";
            char b = 'B';
            char y = 'y';
            char e = 'e';
            string stringInput2 = "I love programming";

            Console.WriteLine($"" +
                              $"{stringInput1}" +
                              $"{Environment.NewLine}" +
                              $"{b}" +
                              $"{Environment.NewLine}" +
                              $"{y}" +
                              $"{Environment.NewLine}" +
                              $"{e}{Environment.NewLine}" +
                              $"{stringInput2}");
        }
    }
}
