namespace PracticeIntegerNumbers
{
    using System;

    public class PracticeIntegerNumber
    {
        public static void Main()
        {
            sbyte sByteNumber = -100;
            Console.WriteLine(sByteNumber);

            byte byteNumber = 128;
            Console.WriteLine(byteNumber);

            short shortNumber = -3540;
            Console.WriteLine(shortNumber);

            ushort ushortNumber = 64876;
            Console.WriteLine(ushortNumber);

            uint uintNumber = 2147483648;
            Console.WriteLine(uintNumber);

            int intNumber = -1141583228;
            Console.WriteLine(intNumber);

            long longNumber = -1223372036854775808;
            Console.WriteLine(longNumber);
        }
    }
}
